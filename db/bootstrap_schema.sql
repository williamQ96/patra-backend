CREATE TABLE IF NOT EXISTS support_tickets (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  subject text NOT NULL,
  category text NOT NULL DEFAULT 'General',
  priority text NOT NULL DEFAULT 'Medium',
  status text NOT NULL DEFAULT 'open',
  description text NOT NULL,
  submitted_by text NOT NULL,
  submitted_at timestamptz NOT NULL DEFAULT NOW(),
  admin_response text,
  reviewed_by text,
  reviewed_at timestamptz,
  updated_at timestamptz NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_support_tickets_status ON support_tickets (status);
CREATE INDEX IF NOT EXISTS idx_support_tickets_submitted_by ON support_tickets (submitted_by);
CREATE INDEX IF NOT EXISTS idx_support_tickets_submitted_at ON support_tickets (submitted_at DESC);

CREATE TABLE IF NOT EXISTS submission_queue (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  submission_type text NOT NULL,
  status text NOT NULL DEFAULT 'pending',
  submitted_by text NOT NULL,
  submitted_at timestamptz NOT NULL DEFAULT NOW(),
  title text,
  data jsonb NOT NULL,
  asset_payload jsonb NOT NULL DEFAULT '{}'::jsonb,
  intake_method text,
  submission_origin text,
  admin_notes text,
  reviewed_by text,
  reviewed_at timestamptz,
  created_asset_id bigint,
  created_asset_type text,
  error_message text,
  updated_at timestamptz NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_submission_queue_status ON submission_queue (status);
CREATE INDEX IF NOT EXISTS idx_submission_queue_submitted_by ON submission_queue (submitted_by);
CREATE INDEX IF NOT EXISTS idx_submission_queue_submitted_at ON submission_queue (submitted_at DESC);
CREATE INDEX IF NOT EXISTS idx_submission_queue_type ON submission_queue (submission_type);

CREATE TABLE IF NOT EXISTS generated_dataset_artifacts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  artifact_key text NOT NULL UNIQUE,
  title text NOT NULL,
  source_dataset_id text NOT NULL,
  submitted_by text,
  planner_mode text NOT NULL DEFAULT 'deterministic',
  query_schema jsonb NOT NULL,
  generated_schema jsonb NOT NULL,
  derivation_plan jsonb NOT NULL,
  validation_report jsonb NOT NULL,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  output_csv_path text NOT NULL,
  output_schema_path text NOT NULL,
  status text NOT NULL DEFAULT 'generated',
  review_submission_id bigint REFERENCES submission_queue(id),
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_generated_dataset_artifacts_source_dataset_id ON generated_dataset_artifacts (source_dataset_id);
CREATE INDEX IF NOT EXISTS idx_generated_dataset_artifacts_status ON generated_dataset_artifacts (status);

CREATE TABLE IF NOT EXISTS experiments (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  start_at timestamptz NOT NULL,
  submitted_at timestamptz,
  executed_at timestamptz,
  model_used_at timestamptz,
  total_images integer,
  total_predictions integer,
  total_ground_truth_objects integer,
  true_positives integer,
  false_positives integer,
  false_negatives integer,
  precision numeric(6,5),
  recall numeric(6,5),
  f1_score numeric(6,5),
  mean_iou numeric(6,5),
  map_50 numeric(6,5),
  map_50_95 numeric(6,5),
  created_at timestamptz NOT NULL,
  updated_at timestamptz NOT NULL,
  user_id bigint NOT NULL REFERENCES users(id),
  edge_device_id bigint NOT NULL REFERENCES edge_devices(id),
  model_id bigint NOT NULL REFERENCES models(id)
);
CREATE INDEX IF NOT EXISTS idx_experiments_user_id ON experiments (user_id);
CREATE INDEX IF NOT EXISTS idx_experiments_edge_device_id ON experiments (edge_device_id);
CREATE INDEX IF NOT EXISTS idx_experiments_model_id ON experiments (model_id);
CREATE INDEX IF NOT EXISTS idx_experiments_start_at ON experiments (start_at);

CREATE TABLE IF NOT EXISTS raw_images (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  image_name text NOT NULL,
  ground_truth jsonb,
  created_at timestamptz NOT NULL,
  updated_at timestamptz NOT NULL
);

CREATE TABLE IF NOT EXISTS experiment_images (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  experiment_id bigint NOT NULL REFERENCES experiments(id),
  raw_image_id bigint NOT NULL REFERENCES raw_images(id),
  image_count integer,
  image_received_at timestamptz,
  image_scored_at timestamptz,
  image_store_deleted_at timestamptz,
  image_decision text,
  top_label text,
  top_probability numeric(8,7),
  ingested_at timestamptz NOT NULL,
  scores jsonb,
  UNIQUE (experiment_id, raw_image_id)
);
CREATE INDEX IF NOT EXISTS idx_experiment_images_experiment_id ON experiment_images (experiment_id);
CREATE INDEX IF NOT EXISTS idx_experiment_images_raw_image_id ON experiment_images (raw_image_id);
CREATE INDEX IF NOT EXISTS idx_experiment_images_received_at ON experiment_images (image_received_at);
CREATE INDEX IF NOT EXISTS idx_experiment_images_ingested_at ON experiment_images (ingested_at);
